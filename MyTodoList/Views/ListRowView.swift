import SwiftUI

struct ListRowView: View {
    
    let item: ItemModel
    
    var body: some View {
        HStack {
            Image(systemName: item.isCompleted ? "checkmark.circle" : "circle")
                .foregroundColor(item.isCompleted ? .accentColor : .red)
            Text(item.title)
            Spacer()
        }
        .font(.title2)
        .padding(.vertical, 8)
        .accessibilityIdentifier(item.title)
    }
}

struct ListRowView_Previews: PreviewProvider {
    static var item1 = ItemModel(title: "First item!", isCompleted: false, username: "")
    static var item2 = ItemModel(title: "Second item!", isCompleted: true, username: "")
    
    static var previews: some View {
        Group {
            ListRowView(item: item1)
                .previewInterfaceOrientation(.landscapeLeft)
            ListRowView(item: item2)
        }
        .previewLayout(.sizeThatFits)
    }
}
